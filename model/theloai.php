    public function themtheloai($theloai)
    {
        $db = DATABASE::connect();
        try {
            $sql = "INSERT INTO theloai(TenTL, TrangThai) 
VALUES(:TenTL, :TrangThai)";
            $cmd = $db->prepare($sql);
            $cmd->bindValue(':TenTL', $theloai->TenTL);
            $cmd->bindValue(':TrangThai', $theloai->TrangThai);
            $cmd->execute();
            $MaTL = $db->lastInsertMaTL();
            return $MaTL;
